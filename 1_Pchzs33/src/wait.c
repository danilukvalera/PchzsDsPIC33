//*****************************************************************************
// Задержка на 1 мс
void wait1ms( void )
{ 
    uint8_t i;
    for (i = 0; i < 8; i++) {
        wait125mks();
    }
}

//*****************************************************************************
// Задержка на 10 мс
void wait10ms( void )
{ 
    uint16_t i;
    for (i = 0; i < 10; i++) {
        wait1ms();
    }
}
