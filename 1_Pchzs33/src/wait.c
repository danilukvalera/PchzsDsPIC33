//*****************************************************************************
// Задержка на 250 мкс
void wait250mks( void )
{ 
    wait62mks();
    wait62mks();
    wait62mks();
    wait62mks();
}

//*****************************************************************************
// Задержка на 125 мкс
void wait125mks( void )
{ 
    wait62mks();
    wait62mks();
}

//*****************************************************************************
// Задержка на 62,5 мкс
void wait62mks( void )
{ 
    __delay32( ((F_CY/10UL)*625UL)/1000000UL );
}

//*****************************************************************************
// Задержка на 1 мкс
void wait1mks( void )
{ 
    __delay32( F_CY / 1000000UL );
}

